    <h3 title="Info">Info</h3>
    <p>L'idea del glossario nacque da un progetto di scuola diretto dal professor Lorenzini. Lo scopo dell'attività era creare delle pagine
    web in grado di mostrare all'utente determinate definizioni, inerenti a termini specifici su diversi argomenti che a loro volta appartengono a diverse
    materie di scuola. L'attività era stata pensata per imparare ad utilizzare un database MYSQL con il linguaggio PHP e per ottenere un qualcosa
    di utile per lo studente. Finimmo per lavorarci in 2, bel mondo la scuola vero?</p>
    <p>Io mi occupavo della parte frontend, mentre il mio compare (Barber se ci sei batti un colpo) della probabilmente 
    più complessa parte backend per un eventuale amministratore. 
    Tutto sommato venne un buon lavoro, considerando il poco tempo a disposizione e le conoscenze di allora. </p>
    <p>A oltre un anno di distanza e con qualche esperienza/conoscenza in più, ho deciso di riscrivere il glossario in 
    entrambe le sue parti, sfruttando la tecnica AJAX. Quindi oltre ai classici HTML,CSS,PHP si è aggiunto anche un bel po' di JavaScript.
    Per quanto riguarda il database è sostanzialmente rimasto lo stesso, poichè fu pensato con criterio e quindi non mi ha creato problemi riutilizzarlo.</p>
